/*
Loop Structure Contain Three Loops :
1) for : 
syntax: 
for (size_t i = 0; i < count; i++)
{
    // code to be executed
}
*/

/*
Is Used when the number of iterations is known beforehand : 
It Contains of Three Parts : initialization , condition and increment/decrement 

for (initialization; condition; increment/decrement) {
    // Code to be executed
}

*/



/* 2)While :
Is used when the number of ietration is not known beforehand . it continues to execute as long as the condition is true 
syntax :
while (condition) {
    // Code to be executed
}
 
 */



/*
3) d0-while : Is Similar to the while loop , but it guarantees that the code block is executed at least once because the condition is checked after the loop Body 
syntax :
do {
// Code to be executed
} while (condition);    

*/
